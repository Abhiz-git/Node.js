function add(no1,no2)
{
    return no1+no2;
}
function sub(no1,no2)
{
    return no1-no2;
}
function div(no1,no2)
{
    return no1/no2;
}
function mult(no1,no2)
{
    return no1*no2;
}

module.exports={
    Addition: add,
    Subtraction: sub,
    Division: div,
    Multiplication: mult
}
